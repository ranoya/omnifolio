<div class='tabelapesquisa2'>
    <div class='tablehead'>ONTOLOGIA</div>
    <div class='tablehead'>DESCRIÇÃO</div>

    <div><b>Computação afetiva</b></div>
    <div>Artefatos destinados a sensibilizar usuários, simular/comunicar emoções ou responder a elas de alguma maneira.</div>

    <div><b>Biohacking</b></div>
    <div>Circuitos, microprocessadores e dispositivos implantados no corpo ou destinados a alterar as funções corporais.</div>

    <div><b>Código criativo</b></div>
    <div>Artefatos destinados a expressividade gráfica, sonora, audiovisual ou multimidiática, geralmente com propósito artístico ou poético.</div>

    <div><b>Multimídia</b></div>
    <div>Dispositivos voltados para informação ou apresentação de informações, dotados de recursos multimidiáticos e geralmente para consultas públicas como quiosques, guias interativos ou catálogos.</div>

    <div><b>Narrativas interativas</b></div>
    <div>Transformação dos tradicionais suportes de histórias, notícias ou relatos, como jornais, revistas e livros, transformados em aparatos computacionais para que essas narrativas interajam de maneiras diversas com os usuários.</div>

    <div><b>Jogos</b></div>
    <div>Artefatos computacionais de aspecto lúdico, geralmente com a função de entreter de formas variadas em relação à imersão, competitividade e expressão.</div>

    <div><b>Interfaces hápiticas</b></div>
    <div>Artefatos cuja interface com o usuário se dá através de estímulos táteis ou através de estímulos físicos de alguma natureza como sopro, vento ou som.</div>

    <div><b>Interfaces tangíveis</b></div>
    <div>Artefatos cuja interface com o usuário se dá através de objetos concretos, das formas mais variadas, que são capazes de se comunicar com o processamento computacional como dispositivos de entrada e/ou saída.</div>

    <div><b>Reconhecimento de imagem</b></div>
    <div>Capacidade de reconhecer objetos, pessoas, ações, lugares ou superfícies, e incorporar este reconhecimento como parte da interação com o artefato.</div>

    <div><b>Inteligência artificial</b></div>
    <div>Artefatos capazes de interpretar o que o usuário quer ou está tentando realizar, e interagir com ele nível semântico. São geralmente capazes de registrar padrões e hábitos dos usuários para aperfeiçoar sua própria interação.</div>

    <div><b>Video mapping</b></div>
    <div>Projeções visuais e audiovisuais sobre superfícies ou espaços, capazes de interagir com estes suportes ou com usuários através deles.</div>

    <div><b>Instalações</b></div>
    <div>Artefatos e produções computacionais que ocupam grandes espaços e interagem, geralmente, com múltiplas pessoas ao mesmo tempo. Podem variar de artefatos decorativos à obras de arte do tipo <i>site specific</i>.</div>

    <div><b>Performances</b></div>
    <div>Artefatos computacionais dos mais variados projetados para interagir com apresentações, danças, peças de teatro e demais tipos de atividades performáticas.</div>

    <div><b>Robótica / Cinética</b></div>
    <div>Equipamentos e mecanismos articulados controlados por microprocessadores ou outras formas de computação, que interagem fisicamente com usuários.</div>

    <div><b>Computação física</b></div>
    <div>Computação e processamento computacional destinado a interagir com usuários de maneira física e concreta através de sensores e representações materiais, geralmente transformados em objetos do cotidiano.</div>

    <div><b>Computação vestível</b></div>
    <div>Dispositivos computacionais transformados em indumentária, como roupas, acessórios e adereços, para serem vestidos e/ou utilizados no corpo de maneira ubíqua e não-invasiva.</div>

    <div><b>Quantified self</b></div>
    <div>Sensores e dispositivos computacionais acoplados ao corpo, transformados em indumentária ou conectados aos usuários de outras maneiras, projetados para medir constantemente dados vitais e informações corpóreas, e para proporcionar melhorias, avaliações e dados sobre as atividades do usuário.</div>

    <div><b>Realidade virtual</b></div>
    <div>Artefatos construídos para interagir dentro (ou como) de ambientes tridimensionais simulados e plenamente artificiais, capazes de produzir uma realidade sintética, imersiva e alternativa ao mundo concreto e sensível.</div>

    <div><b>Realidade aumentada</b></div>
    <div>Muitas vezes tratada como realidades mistas, é a sobreposição de um ambiente tridimensional artificial e simulado, sobre a realidade concreta captada pelos sentidos, através de aparatos capazes de projetar esta realidade artificial sobre nossos sentidos.</div>

