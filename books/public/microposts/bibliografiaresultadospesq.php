<h2>2016-2018 | Computação criativa / arte generativa</h2>

<div class="corpus">

    <p>As tentativas em compreender processos produtivos e métodos projetuais para estes artefatos me levaram, em 2016, a um "ajuste fino", observando questões e elementos mais sutis nestes artefatos. Inicialmente, identifiquei que ontologias como "inteligência artificial" e/ou a análise semântica de instruções (a exemplo dos assistentes pessoais nos dispositivos mobile e os chatbots) passaram a incorporar - integral ou parcialmente - muitos dos projetos em desenvolvimento. Posteriormente, verifiquei que haviam elementos importantes no "Código criativo" (parametrizações, autômatos e processos generativos que tornavam seu processo projetual único.</p>

    <p>A investigação passou a se concentrar naquilo que passou a ser chamado de "Computação criativa", envolvendo principalmente as ontologias de "Código criativo", "Jogos", "Multimídia", "Computação afetiva", "Narrativas interativas" e "Performance", como um conjunto de práticas projetuais trabalhando aspectos próximos.</p>

</div>

