<div class='tabelapesquisa2'>
    <div class='tablehead'>ASPECTO</div>
    <div class='tablehead'>DESCRIÇÃO</div>

    <div><b>Criativo</b></div>
    <div>Por aspecto criativo entende-se a forma de encantamento por um objeto quando associado a uma dimensão de criatividade artística ou expressiva, devido a seu comportamento inusitado estimular novas perspectivas e sentidos como uma obra de arte é capaz de fazer.</div>

    <div><b>Inovador</b></div>
    <div>Por aspecto inovador entende-se a forma de encantamento por um objeto por sua inventividade ou pela capacidade de mudar significações, devido a forma como ele objetivamente muda as relações estabelecidas com outras coisas em nossa experiência.</div>

    <div><b>Emocional</b></div>
    <div>Por aspecto emocional entende-se a forma de encantamento por um objeto quando ele é considerado divertido, excitante, engraçado ou quando nos sensibiliza de alguma forma; o aspecto emocional diz respeito a capacidade de um artefato provocar nossas emoções.</div>

    <div><b>Prazer de uso</b></div>
    <div>Por prazer de uso entende-se a capacidade de um artefato nos encantar ao proporcionar o sentimento de prazer em sua manipulação, seja por sua mecânica de funcionamento, seja por outras formas articuladas de realizar isso.</div>

    <div><b>Lúdico</b></div>
    <div>Por aspecto lúdico entende-se o encantamento em decorrência do artefato instituir um sentido de brinquedo, de brincar ou jogar, ou ser capaz de provocar uma atitude / espírito / ambiência de quem se dispõe a entrar em um jogo ou em uma brincadeira.</div>

    <div><b>Imersivo</b></div>
    <div>Por aspecto imersivo entende-se o encantamento causado por conta de um envolvimento profundo decorrente de um artefato que proporcione uma imersão, seja ela física ou simbólica.</div>

    <div><b>Hipnótico</b></div>
    <div>Por aspecto hipnótico entende-se uma capacidade intensa do artefato em captar a atenção de seu espectador, no limite, fazendo com que perca a percepção do tempo despendido nesta relação.</div>

    <div><b>Sublime</b></div>
    <div>Por sublime se entende um efeito de encantamento arrebatador sobre um sujeito, as vezes compreendido como uma sedução tão intensa a ponto de focalizar todo tipo de atenção e interesse para o objeto como se nada existisse além dele. Isto pode ser resultado de questões formais / viscerais, como por questões comportamentais ou reflexivas.</div>


