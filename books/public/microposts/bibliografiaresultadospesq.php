<div class="corpus">

    <p>Os produtos da pesquisa sobre ensino de dados para o design de visualização da informação são de ordem mais reflexiva e crítica do que em conformações como produtos gráficos, materiais, e/ou levantamentos qualitativos ou quantitativos. Como parte do método aplicado, a disciplina de <a href='javascript:openMeta(' and');'>Editorial, HQ e artefatos narrativos digitais</a> foi utilizada como espaço experimental para se entender as melhores práticas que poderiam ser adotadas para esse fim. No primeiro experimento, em 2022, a proposta foi ensinar os fundamentos e as práticas das Ciências de Dados como forma de habilitar estudantes de design a produzirem visualizações, domínio que uma vez adquirido poderia ser utilizado nos projetos de design. Esta hipótese não poderia estar mais equivocada: os estudantes não conseguiam correlacionar aquilo que estavam aprendendo sobre dados com suas próprias atividades como designers, e ao contrário do esperado, não eram capazes de incorporar este novo conhecimento como algo útil (e necessário) à sua prática.</p>

    <p>A inabilidade de articulação de novos conhecimentos, alheios originalmente ao campo formativo do design, como uma forma de ampliação de seu exercício profissional rumo à práticas multidisciplinas, é sinal de que o próprio ensino de base do design possui entraves problemáticos; a experiência evidenciou que a formação atual dos designers, ao menos dentro do curso da UFPE, possuia problemas severos sobre a fundamentação do que se constitui Design da Informação; também deixou claro que o ensino deste campo se fechou em uma prática endógena e hermética, incapaz de atuar de maneira multidisciplinar, articulando outros saberes e outras práticas, como deveria promover o ensino acadêmico do design.</p>


    <p>A proposta de atuação, já em 2023, deslocou-se da tentativa de habilitar estudantes com competências em Ciências de Dados, para uma abordagem onde as Ciências de Dados seriam envolvidas "na medida da necessidade". A reformulação se centrou no desenvolvimento de projetos editoriais digitais com dados, permitindo aos estudantes que elaborassem seus próprios projetos editoriais, e que o entendimento sobre dados surgisse pela necessidade, conforme os projetos amadureciam e demandavam por maior estruturação e relacionamento das informações.</p>

    <p>Os resultados foram significativamente melhores do que a primeira versão, mas ainda longe de algo minimamente satisfatório dentro do problema de visualização:</p>

