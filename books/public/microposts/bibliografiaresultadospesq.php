    <p>
        De carona nos diversos esforços de mapeamento e registro/memória, também desenvolvi nesse momento dois trabalhos de Arqueologia Digital que se iniciaram na tentativa de mapear as origens da relação entre a visualização de dados, o mercado corporativo, e o nascimento da indústria editorial digital. No epicentro desta questão se encontrava o conceito de <i>Business Graphics</i>, datado da década de 1980, e difundido pelo setor nascente da informática pessoal - sobretudo pela <i>Apple Computer, Inc</i> - que buscava integrar uma cadeia logistica do processamento de dados corporativos, do seu levantamento, à sua publicação. Esta investigação produziu a organização de dois materiais multimidiáticos, um contendo a ampla gama de <a target='_blank' href='https://omnidocs.vercel.app/materiais/arqueologiadesign'>ferramentas computacionais de design</a> utilizadas nas décadas de 1980 e 1990, e outro mais específico às <a target='_blank' href='https://omnidocs.vercel.app/materiais/arqueologiaeditorial'>ferramentas editoriais</a>, nesta interseção entre visualizações, corporações, e industria editorial. Inspirado pelas apresentações do professor Guilherme Cunha Lima no evento Terças de Design promovida pelo Laboratório de Práticas Gráficas da UFPE (LPG), e da professora Edna Cunha Lima, promovida pelo Papo Dado (PUC-Rio), ambos sobre as ferramentas e processos de design antes do uso de computadores, o trabalho arqueológico foi também aproveitado para mostrar a fase subsequente nas práticas do design, com a introdução incipiente do computador e suas ferramentas, ainda pouco familiares aos profissionais brasileiros no período. <b>1</b> palestra intitulada <a target='_blank' href='https://www.youtube.com/watch?v=4_Vbda-V2Uk'>Design e o Uso do Computador</a> foi realizada através do Laboratório Lexus da UFRN.
