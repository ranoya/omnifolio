<div class="corpus">

    <p>Ao longo do envolvimento com estes artefatos voltados à expressão, o problema da significação e sentido (as questões simbólicas e semânticas, mais do que os aspectos sintáticos ou a forma) emergiram. A "Computação criativa", e seus desdobramentos, se constituem como artefatos exóticos não por sua forma ou uso, mas em como produzem, através da interação, sentidos e experiências. Reconhecer essa sutil mudança exigiu novas categorias, agora sob aspectos mais finos e menos molares. O olhar apurado de Paola Antonelli, na época curadora do The Museum of Modern Art (MoMA) de Nova York para artefatos computacionais, compactua com os entendimentos obtidos até este momento:
    </p>

    <p><a class="citacao" href="javascript:openMeta('antonelli');">“Em contraste com o triunfo da semiótica no século vinte, a qual rebaixou a comunicação como nada além da transmissão mecânica de significados codificados, o século vinte um se iniciou com a pan-comunicação – tudo e todos transmitindo conteúdos e significados em todas as combinações possíveis, de um-para-um à tudo-para-todos. Nós agora esperamos que objetos se comuniquem, uma mudança cultural tornada evidente quando vemos crianças procurando por botões ou sensores em um um objeto novo, mesmo quando este objeto não possui baterias ou plugues. Fale comigo: design e comunicação entre pessoas e objetos, enfrenta este desenvolvimento importante do fim do século vinte na cultura do design, que pode ser descrita como uma mudança na centralidade da função para a significação, e no foco do século vinte um nas necessidades de se comunicar para uma condição de existir. Por esta nova perspectiva, todos os objetos ocupam uma posição única na cultura material, e todos eles possuem informações além de seu uso ou aparência imediatos. Não é mais suficiente para os designers de hoje balancearem forma e função, assim como não é suficiente atribuirem significados. [...] como Khoi Vinh aponta [...] Coisas podem se comunicar com pessoas, mas os designers escrevem o roteiro inicial sobre o qual nós desenvolvemos ou improvisamos o diálogo."</a></p>

</div>

