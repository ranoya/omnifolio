reu em forma de uma mesa <a target='_blank' href='https://www.youtube.com/watch?v=-BI1NFiTVKY'>redonda sobre visualização</a> no 2º Simpósio de Pesquisa do PPGDesign [Design em Fronteiras] onde, junto com a equipe do laboratório, discutimos as diferentes experiências de ensino em visualização de dados / informação.
