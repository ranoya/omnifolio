<div class="corpus">

  <p>
    A <a href="javascript:openMeta('esteticainteracao');">pesquisa em Estética da Interação</a> é uma decorrência da conclusão em 2013 da investigação durante o doutorado intitulada <a href='https://drive.google.com/file/d/0B8OiJVdB8D6wejMzREowLXVCNWM' target='_blank'>A intuitividade nos artefatos interativos</a>, realizada na Escola de Comunicação e Artes da Universidade de São Paulo, sob orientação do <a href="javascript:openMeta('mauro');">Prof. Dr. Mauro Wilton de Souza</a>. Sua continuação lógica se dava na investigação de artefatos - ou mais precisamente das mídias - computacionais em seus formatos considerados "estranhos" ou "exóticos", isto é, aqueles que fugiam das convenções nela pré-estabelecidas e/ou de seu paradigma bem estabelecido e circunscito entre aplicativos, websites, softwares, telas, teclados (virtuais ou tangíveis) e mouse (ou touchscreen), já familiares e talvez quase naturalizados para a interação entre usuários e dispositivos.
  </p>

  <p>Neste memorial, tento apresentar um pouco deste trabalho relacionando a atividade de pesquisa a minha prática profissional e acadêmica. O <a target="_blank" href="https://www.ranoya.com/pt/pesquisa/jornadacompleta.php">relatório completo</a> das atividades está disponível publicamente para os interessados em uma abordagem mais detalhada, incluíndo os resultados obtidos durante as pesquisas e todos os seus produtos..

