    De carona nos diversos esforços de mapeamento e registro/memória, também desenvolvi nesse momento dois trabalhos de Arqueologia Digital que se iniciaram na tentativa de mapear as origens da relação entre a visualização de dados, o mercado corporativo, e o nascimento da indústria editorial digital. No epicentro desta questão se encontrava o conceito de <i>Business Graphics</i>, datado da década de 1980, e difundido pelo setor nascente da informática pessoal - sobretudo pela <i>Apple Computer, Inc</i> - que buscava integrar uma cadeia logistica do processamento de dados corporativos, do seu levantamento, à sua publicação. Esta investigação produziu a organização de <b>3</b> materiais multimidiáticos, o primeiro contendo a ampla gama de <a target='_blank' href='https://omnidocs.vercel.app/materiais/arqueologiadesign'>ferramentas computacionais de design</a> utilizadas nas décadas de 1980 e 1990, o segundo mais específico às <a target='_blank' href='https://omnidocs.vercel.app/materiais/arqueologiaeditorial'>ferramentas editoriais</a>, e o terceiro reunindo as <a href='https://slidelines.vercel.app/level/?s=init&allowverticalscroll=true&file=https://opensheet.elk.sh/1Hja-7ozKTpcfhVX9sc3FkDH-NLj_RXllFIDTO8EIFV0/ApresentacaoSlidesOutline&theme=https://slidelines.vercel.app/styles/fit.css' target='_blank'>ferramentas para charts e slides</a>, nesta interseção entre visualizações, corporações, e industria editorial. Inspirado pelas apresentações do professor Guilherme Cunha Lima no evento Terças de Design promovida pelo Laboratório de Práticas Gráficas da UFPE (LPG), e da professora Edna Cunha Lima, promovida pelo Papo Dado (PUC-Rio), ambos sobre as ferramentas e processos de design antes do uso de computadores, o trabalho arqueológico foi também aproveitado para mostrar a fase subsequente nas práticas do design, com a introdução incipiente do computador e suas ferramentas, ainda pouco familiares aos profissionais brasileiros no período. <b>1</b> palestra intitulada <a target=' _blank' href='https://www.youtube.com/watch?v=4_Vbda-V2Uk'>Design e o Uso do Computador</a> foi realizada através do Laboratório Lexus da UFRN.
  </p>

</div>

<div class="symbol"></div>

<div class="corpus">

  <p>Os produtos da <a href="javascript:openMeta('pesquisacsa');">pesquisa sobre ensino de dados para o design de visualização da informação</a> são de ordem mais reflexiva e crítica do que em conformações como produtos gráficos, materiais, e/ou levantamentos qualitativos ou quantitativos. Como parte do método aplicado, a disciplina de <a href="javascript:openMeta('and');">Narrativas inovadoras com dados na comunicação digital</a> foi utilizada como espaço experimental para se entender as melhores práticas que poderiam ser adotadas para esse fim. No primeiro experimento, em 2022, a proposta foi ensinar os fundamentos e as práticas das Ciências de Dados como forma de habilitar estudantes de design a produzirem visualizações, domínio que uma vez adquirido poderia ser utilizado nos projetos de design. Esta hipótese não poderia estar mais equivocada: os estudantes não conseguiam correlacionar aquilo que estavam aprendendo sobre dados com suas próprias atividades como designers, e ao contrário do esperado, não eram capazes de incorporar este novo conhecimento como algo útil (e necessário) à sua prática.</p>

  <p>A inabilidade de articulação de novos conhecimentos, alheios originalmente ao campo formativo do design, como uma forma de ampliação de seu exercício profissional rumo à práticas multidisciplinares, é sinal de que o próprio ensino de base do design possui entraves problemáticos; a experiência evidenciou que a formação atual dos designers, ao menos dentro do curso da UFPE, possuia problemas severos sobre a fundamentação do que se constitui Design da Informação; também deixou claro que o ensino deste campo se fechou em uma prática endógena e hermética, incapaz de atuar de maneira multidisciplinar, articulando outros saberes e outras práticas, como deveria promover o ensino acadêmico do design.</p>


  <p>A proposta de atuação, já em 2023, deslocou-se da tentativa de habilitar estudantes com competências em Ciências de Dados, para uma abordagem onde as Ciências de Dados seriam envolvidas "na medida da necessidade". A reformulação se centrou no desenvolvimento de projetos editoriais digitais com dados, permitindo aos estudantes que elaborassem seus próprios projetos editoriais, e que o entendimento sobre dados surgisse pela necessidade, conforme os projetos amadureciam e demandavam por maior estruturação e relacionamento das informações.</p>

  <p>Os resultados foram significativamente melhores do que a primeira versão, mas ainda longe de algo minimamente satisfatório dentro do problema de visualização.</p>

</div>

<div class="symbol"></div>

<div class="corpus">

  <p>Novamente, as deficiências apresentadas pelos estudantes circunscreviam questões ainda mal resolvidas quanto ao próprio Design da Informação: mostravam dificuldades para compreensão do que se enquadraria em um projeto editorial, sobretudo em contexto digital, já que em um mundo onde tudo é apenas " website" (seja um software complexo ou um mero material de divulgação publicitária), os entendimentos dos formatos midiáticos, dos seus papeis, e de seus efeitos, perdem completamente a nitidez taxonômica. Muitos destes estudantes careciam de repertório de referência, não possuindo qualquer familiaridade com artefatos editoriais, já que os jornais ou revistas não faziam mais parte de seu dia-a-dia (confundindo-se, novamente, com "websites" ). Apesar de ser plenamente compreensível que os artefatos que não participam da prática cultural dos sujeitos lhes sejam estranhos, e não se constitua facilmente qualquer domínio técnico prévio sobre seus aspectos e pormenores projetuais, o design editorial é fundamento básico para a prática do Design da Informação, sem o qual é impossível atuar competentemente nele; tratam-se de evidências concretas de que os designers não estão sendo preparados adequadamente para atuarem em qualquer atividade gráfica, e de que carregam deficiências formativas estruturais (ao menos no que tange o esquema de ensino ao qual estão submetidos nesta instituição).</p>

  <p>O terceiro experimento envolveu o mesmo princípio centrado em um projeto onde as Ciências de Dados entram como coadjuvantes e são exploradas "na medida da necessidade". Para este caso, a atuação se deu em nível de pós-graduação onde dados (de pesquisa) já se fazem presentes, e demandam por formas de visualização para que sejam adequadamente comunicados. A diferença sensível entre os dois casos não é que o nível de pós-graduação garanta alguma formação substancialmente melhor aos estudantes-pesquisadores, mas de que o risco de não comunicarem adequadamente seus resultados comprometa os projetos aos quais estão dedicados por 2, ou até 4 anos nos casos dos doutorandos. Riscos mais altos em jogo produzem uma alta pressão pela aquisição e aplicação bem articulada do conhecimento - em conjunto a problemas de saúde física e psíquica, inerentes e quase inevitáveis em processos formativos nestes termos.</p>



</div>

<div class="symbol"></div>

<div class="corpus">

  <p>Finalmente, um quarto experimento realizado na graduação mesclou abordagem da <a href="javascript:openMeta('projetao');">metodologia Projetão</a>, desenvolvida previamente junto ao grupo de inovação do Centro de Informática (CIn), com as atividades de pesquisa aplicadas diretamente na disciplina de <a href="javascript:openMeta('and');">Narrativas inovadoras com dados na comunicação digital</a>, fazendo dela uma experiência multidisciplinar realizada em conjunto aos cursos de Jornalismo e Computação. Nesta configuração, busquei verificar se os impedimentos e dificuldades residiam na inabilidade dos estudantes em articularem outras questões externas ao design no aprendizado com dados, isto é, se aquilo que dificultava a apreensão dos conceitos e princípios de dados dentro do design não seriam, na verdade: 1) a dificuldade de elaborar uma narrativa pela ausência do aprendizado de comunicação e seus conceitos; 2) a dificuldade de entenderem o que seria (ou não seria) possível fazer, por não dominarem as técnicas construtivas necessárias, e; 3) a dificuldade de enxergar as aplicações e usos deste tipo de projeto. Considerando esta hipótese, a elaboração de projetos reais (e não a sua simulações através de protótipos) em colaboração com outros profissionais em formação produziriam necessidades tangíveis e um interesse imediato (concreto e aplicável) para o engajamento com o conhecimento envolvido na visualização da informação.</p>

  <p>Os dois últimos experimentos assumiram uma abordagem prática e aplicada para o ensino. A disciplina de pós-graduação Design da Informação e Visualização de Dados para a Pesquisa se baseou em uma metodologia de pesquisa-ação, onde se constrói o saber a partir do fazer e da experimentação; já a versão multidisciplinar de <a href="javascript:openMeta('and');">Narrativas inovadoras com dados na comunicação digital</a> se baseou em uma metodologia de inovação cujo princípio é produzir aperfeiçoamentos avançando o projeto rumo ao desconhecido, testando ideias e proposições para as quais não há referências prévias.</p>



</div>


