<h2>2014-2016 | Computação vestível e os artefatos "além do sandbox"</h2>

<div class="corpus">
  <p>Neste período tratei das interações exóticas que não se enquadravam no convencional, provisoriamente, por "computação física" ou "internet of things", mas estes rótulos também acabaram se mostrando limitados, ou, mais especificamente, apenas descreviam dois tipos de artefatos dentre um conjunto diverso de formas de interação com dispositivos de mídia computacional. Apesar deste grupo maior de artefatos compartilharem entre si da computação embarcada, de relações de interação fora do convencional, e de um grande potencial de inovação, é muito difícil atribuir uma denominação comum ou genérica que os agrupe, como facilmente poderíamos agrupar websites, aplicativos mobile e aplicativos desktop com uma denominação de <i>softwares</i>. Na ausência de um termo adequado, acabei tratando estas tipologias apenas como "computação fora da caixinha" ou "computação além da zona de conforto", e finalmente, como "artefatos além do sandbox", em referência aos espaços demarcados sobre os quais temos algum controle - algo que pareceu bastante pertinente se considerarmos que não há qualquer idéia sobre o que estes objetos poderiam se tornar uma vez que escapessem dos enquadramentos e convenções que já temos para eles.
  </p>

  <p>
    A preocupação da pesquisa e os entendimentos que buscava, por ter se tratado de uma investigação com ênfase na questão da configuração do artefato (e não de uso ou consumo), estavam centrados em enquadrar os artefatos com relação às preocupações projetuais específicas de cada categoria, entendendo que os procedimentos, pormenores, e metodologias adequadas para o projeto de cada uma delas seriam também específicos, e não funcionariam para o design de outros artefatos computacionais. Os mapeamentos utilizaram critérios binários (sim/não) para os registros, entendendo que os artefatos poderiam ter características simultâneas em várias das categorias, colocadas, dessa forma, como não excludentes (um mesmo artefato poderia ser a mescla de 2 ou mais categorias). Como resultado deste biênio de pesquisa, foi possível identificar <b>19</b> ontologias (tipos/naturezas) de artefatos exóticos, e mapear suas características através da análise e avaliação de aproximadamente <b>290</b> artefatos.
  </p>

</div>


<h2>2016-2018 | Computação criativa / arte generativa</h2>

<div class="corpus">

  <p>As tentativas em compreender processos produtivos e métodos projetuais para estes artefatos me levaram, em 2016, a um "ajuste fino", observando questões e elementos mais sutis nestes artefatos. Inicialmente, identifiquei que ontologias como "inteligência artificial" e/ou a análise semântica de instruções (a exemplo dos assistentes pessoais nos dispositivos mobile e os chatbots) passaram a incorporar - integral ou parcialmente - muitos dos projetos em desenvolvimento. Posteriormente, verifiquei que haviam elementos importantes no "Código criativo" (parametrizações, autômatos e processos generativos) que tornavam seu processo projetual único.</p>

  <p>A investigação passou a se concentrar naquilo que passou a ser chamado de "Computação criativa", envolvendo principalmente as ontologias de "Código criativo", "Jogos", "Multimídia", "Computação afetiva", "Narrativas interativas" e "Performance", como um conjunto de práticas projetuais trabalhando aspectos próximos.</p>


  <p>Ao longo do envolvimento com estes artefatos voltados à expressão, o problema da significação e sentido (as questões simbólicas e semânticas, mais do que os aspectos sintáticos ou a forma) emergiram. A "Computação criativa", e seus desdobramentos, se constituem como artefatos exóticos não por sua forma ou uso, mas em como produzem, através da interação, sentidos e experiências. Reconhecer essa sutil mudança exigiu novas categorias, agora sob aspectos mais finos e menos molares. O olhar apurado de Paola Antonelli, na época curadora do The Museum of Modern Art (MoMA) de Nova York para artefatos computacionais, compactua com os entendimentos obtidos até este momento:
  </p>

</div>

<p><a class="citacao" href="javascript:openMeta('antonelli');">“Em contraste com o triunfo da semiótica no século vinte, a qual rebaixou a comunicação como nada além da transmissão mecânica de significados codificados, o século vinte um se iniciou com a pan-comunicação – tudo e todos transmitindo conteúdos e significados em todas as combinações possíveis, de um-para-um à tudo-para-todos. Nós agora esperamos que objetos se comuniquem, uma mudança cultural tornada evidente quando vemos crianças procurando por botões ou sensores em um um objeto novo, mesmo quando este objeto não possui baterias ou plugues. Fale comigo: design e comunicação entre pessoas e objetos, enfrenta este desenvolvimento importante do fim do século vinte na cultura do design, que pode ser descrita como uma mudança na centralidade da função para a significação, e no foco do século vinte um nas necessidades de se comunicar para uma condição de existir. Por esta nova perspectiva, todos os objetos ocupam uma posição única na cultura material, e todos eles possuem informações além de seu uso ou aparência imediatos. Não é mais suficiente para os designers de hoje balancearem forma e função, assim como não é suficiente atribuirem significados. [...] como Khoi Vinh aponta [...] Coisas podem se comunicar com pessoas, mas os designers escrevem o roteiro inicial sobre o qual nós desenvolvemos ou improvisamos o diálogo."</a></p>


<h2>2018-2020 | Artefatos narrativos computacionais</h2>

<div class="corpus">

  <p>Em 2018 o trabalho de pesquisa se afunilou em questões ainda mais específicas, direcionadas a uma única tipologia específica: os artefatos narrativos, sejam na forma de notícias, como as revistas ou jornais digitais, sejam livros ou objetos que tentam reproduzir este formato narrativo, ou sejam na forma de jogos, audiovisuais interativos, ou quaisquer outros formatos como uma história é contada através das mídias computacionais. Dos subsídios obtidos com a pesquisa, elaborei materiais interativos e dinâmicos para <a target="_blank" href="https://www.ranoya.com/pt/aulas/">todas as disciplinas oferecidas</a> no Departamento do Design da Universidade Federal de Pernambuco (dDesign/UFPE); também criei, com base no que se produziu pelas investigações, a disciplina de <a href="javascript:openMeta('and');">Narrativas inovadoras com dados na comunicação digital</a>. <b id="matnarrnum"></b> materiais multimidiáticos são resultados diretos desse período da pesquisa.</p>

  <p>Um projeto educacional integrado, intitulado <a href="javascript:openMeta('futuro');">Design para o futuro</a> passou a coordenar disciplinas de graduação elaboradas com o intuito de ensinar estudantes de design a aproveitarem com maior profundidade os recursos computacionais, atuando diretamente com programação para fazer design. Para auxiliá-los, desenvolvi plataformas interativas contendo manuais escritos para referência (<a href="https://github.com/ranoya/ReferenceDocs">referenceDocs</a>) adotados nas disciplinas de <a href="javascript:openMeta('webdesign');">Webdesign</a> e <a href="javascript:openMeta('dgen');">Design Generativo / Paramétrico</a>, e uma plataforma para livros em formato web (<a href="https://github.com/ranoya/sumarion">Sumarion</a>) que acabou incorporando todos estes conteúdos no formato de um livro interativo e dinâmico, chamado <a href="https://www.ranoya.com/books/public/tecnologiascriativas">O tecido das tecnologias criativas</a>, subdividido em <b>7</b> volumes, sendo <b>6</b> deles para o material técnico de referência em cada linguagem específica, e <b>1</b> dedicado à discussões e reflexões sobre a expressão computacional em si.</p>


  <p>Mais um livro interativo foi elaborado e escrito para oferecer conceituação teórico-prática sobre interfaces digitais, contendo questões pertinentes à disciplina de <a href="javascript:openMeta('interfaces');">Design de Interfaces Digitais</a>, que atua de maneira integrada à de <a href="javascript:openMeta('projetao');">Design de Serviços (Projetão)</a></p>

</div>

<h2>2020-2022 | Poéticas computacionais / tecnologias criativas</h2>

<div class="corpus">

  <p>Em 2020, o esforço que empreguei para a experimentação com narrativas computacionais me levou à construção de diversas ferramentas e bibliotecas de programação como meio de viabilizá-las. A necessidade imediata marca um retorno para o trabalho aplicado com as tecnologias criativas, e em especial com seus aspectos expressivos. O período também envolve a escalada da pandemia de Covid-19, onde muitas das práticas de ensino precisaram ser reformuladas para um formato remoto, entremeadas com uma transformação digital forçada, que exigiram experiência sobre questões narrativas e de tecnologias criativas, para que fosse possível integrar interfaces conversacionais, ambientes de livecode e experimentação, mecanismos interativos nos materiais instrucionais, dentre tantas soluções necessárias para prover maior autonomia de aprendizado aos estudantes enfrentando contextos completamente novos de ensino.</p>

  <p>A interrupção do ensino presencial promoveu uma superprodução de materiais audiovisuais. Os registros dos cursos de <a href="javascript:openMeta('futuro');">Webdesign, Design Generativo</a> e <a href="javascript:openMeta('interfaces');">Design de Interfaces Digitais</a> foram colocados online (integralmente), e o curso de <a href="javascript:openMeta('projetao');">Design de Serviços (Projetão)</a> foi transformado em uma plataforma seriada online, com o registro de todas as auals, de todos os resultados (parciais e finais), e do próprio evento de apresentação, todos disponíveis na sua própria plataforma, organizados por semestre (<a target="_blank" href="https://www.projetao.com.br/generator/?g=CIn&dun=timeline">2020.3</a>, <a target="_blank" href="https://www.projetao.com.br/generator/?g=CIn&dun=timeline201">2020.1</a>, <a target="_blank" href="https://www.projetao.com.br/generator/?g=CIn&dun=timeline202">2020.2</a>, <a target="_blank" href="https://www.projetao.com.br/generator/?dun=DemodayCIn20191&g=CIn">Demoday</a>).</p>

  <p>Neste contexto, muito do que se fez em pesquisa adquiriu um caráter mais aplicado, que no campo do design se traduz com a ênfase nos processos de fabricação. As ferramentas construídas neste momento foram o <a target="_blank" href="https://www.ranoya.com/public/eie/?nonew=true#hal">assistente/interface conversacional HAL</a>, uma biblioteca de programação com recursos para construções narrativas chamada de <a target="_blank" href="https://www.ranoya.com/pt/garagem/#bibliotecas">ScrollyTelling Tools</a>, um conjunto de <a target="_blank" href="https://www.ranoya.com/pt/garagem/#playground">ambientes de livecode (playgrounds)</a> para o ensino de programação com as linguagens Javascript, Processing, P5 e Markdown, e ferramentas semelhantes na plataforma de alfabetização em programação (<i>literate programming</i>) <a target="_blank" href="https://observablehq.com">Observable</a>, para as linguagens <a target="_blank" href="https://observablehq.com/@ranoya/p5livecode">P5</a> e <a target="_blank" href="https://observablehq.com/@ranoya/processing-livecode">Processing</a>, utilizando uma solução própria, elaborada para a <a target="_blank" href="https://observablehq.com/@ranoya/processing">implementação destas tecnologias</a>, cuja <a target="_blank" href="https://observablehq.com/@processingbrasil/as-formas-mais-faceis-de-se-utilizar-processing-no-observab">documentação de referência</a> foi traduzida para o <a target="_blank" href="https://observablehq.com/@villares/as-formas-mais-faceis-de-se-utilizar-processing-no-observab">inglês</a> e <a target="_blank" href="https://observablehq.com/@makio135/as-formas-mais-faceis-de-se-utilizar-processing-no-observab">francês</a> pela própria comunidade de <i>software open-source</i>.</p>


  <p>O envolvimento com novos processos de ensino relacionados à pesquisa sobre tecnologias criativas me levou, como consequência, à participação em um <a target="_blank" href="https://www.youtube.com/watch?v=0ucMRGcGvME">encontro sobre ensino de programação criativa</a> com os professores Barbara Castro (na época na PUC-Rio, e posteriormente na ESDI/UERJ), Claudio Esperança (UFRJ), Luisa Pereira (New York University Tisch School of the Arts), Luiz Merkle (UTFPR) e Ricardo Nakamura (USP), mediado pelos também professores Alexandre Villares e Sérgio Venâncio, e organizado pelo <a target="_blank" href="https://garoa.net.br/wiki/P%C3%A1gina_principal">Garoa Hacker Clube</a> em São Paulo. Isso resultou no ingresso ao comitê de organização do evento <a target='_blank' href="https://www.ranoya.com/pt/futuro/#pcdbr">Processing Community Day Brasil</a> e a tentativa de organização da rede <a target='_blank' href="https://www.ranoya.com/public/futuro/?nonew=true#compoetica">Compoética</a> de cooperação acadêmica em tecnologias criativas (um dentre diversos esforços nesse sentido que nunca vingaram).</p>

</div>



<h2>2022-2024 | Visualização de dados / Dataviz</h2>

<div class="corpus">

  <p>
    Muito do esforço em pesquisa e aplicação da pesquisa despontavam para a construção de visualizações de dados, iniciadas bem antes da elaboração da genealogia dos <i>Point & Click Adventures</i>, como as diversas visualizações da grade interativa para o curso de graduação em design do Centro de Artes e Comunicação da UFPE (CAC/UFPE). A experiência na construção de documentos contendo visualização de dados é, inclusive, anterior ao ingresso em projetos de pesquisa.
  </p>

  <p>
    Este campo é, logicamente, uma intersecção entre o design, as ciências de dados e as tecnologias criativas. Infográficos e dataviz foram artefatos extensivamente utilizados durante o período de pandemia.
  </p>


  <p>Este biênio se iniciou com a organização de um laboratório de escopo regional voltado à visualização, em cooperação com os professores Solange Coutinho (UFPE/CAC), Eva Rolim Miranda (UFAL), Rafael de Castro Andrade (UFPR), Ricardo Cunha Lima (UFPE/CAA), e Rodrigo Medeiros (IFPB), o <a href='https://visse.vercel.app' target='_blank'>VISSE: Laboratório de Visualização e Sentidos do Nordeste</a>. A iniciativa se complementa com um subprojeto de pesquisa destinado a analisar <a href="javascript:openMeta('pesquisacsa');">a formação do designer de informação para a elaboração de artefatos de visualização</a>, na tentativa de prover bases para o ensino fundamentado e metodológico destas práticas àqueles que desejam atuar com design da informação.
  </p>

  <p>
    O período também se iniciou com a proposta de uma nova disciplina para o Programa de Pós-Graduação em Design da Universidade Federal de Pernambuco (PPGDesign/UFPE), ministrada em conjunto com os demais professores envolvidos no <a href='https://visse.vercel.app' target='_blank'>VISSE</a>, discutindo os fundamentos do design de informação necessários para uma nova visão (mais narrativa e retórica) sobre os artefatos de visualização, intitulada <a href="javascript:openMeta('dataviz');">Visualização de informação e a construção do sentido</a>. Elaborada como uma disciplina de pesquisa aplicada, ela se propõe a construir este conhecimento em colaboração com os participantes e pesquisadores do programa.
  </p>

  <p>
    As disciplinas de graduação do projeto educacional <a href="javascript:openMeta('futuro');">Design para o futuro</a> também foram reformuladas para colaborar de maneira mais ativa com a formação de designers de informação capazes de atuar na construção de dataviz, e a disciplina de <a href="javascript:openMeta('and');">Narrativas inovadoras com dados na comunicação digital</a> passou a atuar no desenvolvimento de competências narrativas nas mídias computacionais, trazendo também aspectos da ciência de dados, para fomentar a atuação nesse campo por estudantes ainda na graduação.
  </p>

</div>





