  <h1 id='titulocapitulo'>HTML: Guia de Referência</h1>

  <p>Este é um guia de referência para construção de documentos HTML. Utilize o painel com tags e estruturas para localizar a instrução ou procedimento que você precisa de referência. Na versão mobile, utilize a seta na lateral esquerda para acessar o painel.</p>

  <p>Para localizar mais facilmente um tópico, tag ou elemento, digite parte da instrução sobre a linha (campo de entrada) disponível no painel para filtrar os conteúdos deste guia. Entre * para exibir todos eles novamente.</p>

  <p>Este guia é organizado à partir de alguns tópicos:<p>

  <p><b>Estrutura</b><br>Cobre aspectos e instruções formais do código necessárias à todos os documentos;</p>

  <p><b>Head</b><br>Cobre as instruções de meta-informação que devem ser colocadas como cabeçalho nos documentos;</p>

  <p><b>Inline/conteúdo</b><br>Cobre as tags usadas para inserir conteúdo linear e contínuo no documento;</p>

  <p><b>Block</b><br>Cobre as tags usadas para definir blocos e espaços demarcados no documento;</p>

  <p><b>Formulários</b><br>Cobre as tags usadas para construir formulários, blocos e campos de entrada de dados no código HTML;</p>

  <p><b>Todas as tags</b><br>Todas as tags disponíveis relacionadas alfabeticamente.</p>

  <p>Este não é um guia de referência completo contendo todos os elementos da construção do código HTML; ele é apenas um guia inicial, criado para auxiliar estudantes iniciando sua jornada sobre os materiais utilizados para confeccionar documentos apresentados nos navegadores da web. Este guia cobre apenas algumas tags e propriedades mais usuais na produção destes documentos, já dentro da última versão do código HTML, o HTML5. Ele não cobre versões anteriores como o XHTML, ou o HTML 4.01 em suas diferentes versões, não aborda a estrutura XML, ou tags e técnicas já consideradas ultrapassadas mas que são importantes para manter a compatibilidade com sistemas e navegadores antigos (mais comuns do que os projetistas gostariam que fossem).</p>

  <p>Para os iniciantes, o código HTML - <i>Hypertext Markup Language</i> - não é uma linguagem de programação formal. Este tipo de código funciona apenas como um <i>script</i>, um roteiro para que o navegador apresente informações em seu <i>frame/viewport</i> respeitando uma organização de campo definida por regras estabelecidas no código.</p>

  <p>A organização responsável por regular e definir os parâmetros desta linguagem é o W3C - <i>World Wide Web Consortium</i>, e a estrutura completa dos elementos do código HTML pode ser encontrada no seu <a href="https://www.w3.org/TR/html/" target="_blank">manual de referência</a>, mais especificamente em seu capítulo 4. Este manual, contudo, foi criado para desenvolvedores de software (em especial, os desenvolvedores de navegadores web), e não para designers; ele é um mateiral rico em informações técnicas para orientar este grupo, e de difícil compreensão para aqueles cuja perspectiva não é a da computação. Por este motivo, e para suprir as necessidades de designers que precisam de uma instrução direcionada ao que compete a sua própria atividade na construção de documentos HTML, criamos este guia.</p>

