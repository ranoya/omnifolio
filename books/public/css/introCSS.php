<h1 id='titulocapitulo'>CSS: Guia de Referência</h1>

  <p>Este é um guia de referência para construção de especificações CSS. Utilize o painel com propriedades e estruturas para localizar a instrução ou procedimento que você precisa de referência. Na versão mobile, utilize a seta na lateral esquerda para acessar o painel.</p>

  <p>Para localizar mais facilmente um tópico, propriedade ou elemento, digite parte da instrução sobre a linha (campo de entrada) disponível no painel para filtrar os conteúdos deste guia. Entre * para exibir todos eles novamente.</p>

  <p>Este não é um guia de referência completo contendo tudo que está disponível sobre CSS; ele é apenas um guia inicial, criado para auxiliar estudantes iniciando sua jornada sobre os materiais utilizados para confeccionar documentos apresentados nos navegadores da web. Este guia cobre apenas algumas propriedades mais usuais na produção destes documentos, já dentro da última versão do código CSS, o CSS3. Ele não cobre versões anteriores ou tabelas de propriedades disponíveis para cada versão de navegador, são importantes para manter a compatibilidade com sistemas e navegadores antigos (mais comuns do que os projetistas gostariam que fossem).</p>

  <p>Para os iniciantes, o código CSS - <i>Cascade Style Sheets</i> - não é uma linguagem de programação formal. Este tipo de código funciona apenas como um <i>script</i>, um roteiro para que o navegador apresente informações em seu <i>frame</i> respeitando características definidas por regras no código.</p>

  <p>A organização responsável por regular e definir os parâmetros desta linguagem é o W3C - <i>World Wide Web Consortium</i>, e a estrutura completa das propriedades CSS pode ser encontrada no seu <a href="https://www.w3.org/TR/CSS/#indices" target="_blank">manual de referência</a>. Este manual, contudo, foi criado para desenvolvedores, e não para designers; ele é um mateiral rico em informações técnicas para orientar este grupo, e de difícil compreensão para aqueles cuja perspectiva não é a da computação. Por este motivo, e para suprir as necessidades de designers que precisam de uma instrução direcionada ao que compete a sua própria atividade na construção de documentos CSS, criamos este guia.</p>

