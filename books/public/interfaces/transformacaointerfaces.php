        <div class="corpus">
            <p>Alguns aspectos são importantes de serem reconhecidos ao longo das transformações sofridas pela nascente indústria de software no final da década de 1970: o VisiOn não foi a primeira interface gráfica com o usuário a surgir, mas foi a primeira para os PCs. Antes dele, a <a href="javascript:openMeta('xerox');">Xerox</a> já havia criado o computador <a href="javascript:openMeta('alto');">Alto</a>, utilizando um <i>mouse</i> e uma interface gráfica com a plataforma e linguagem <a href="javascript:openMeta('smalltalk');">Smalltalk</a> em 1968 (15 anos antes do VisiOn). O <a href="javascript:openMeta('alto');">Alto</a> era um sistema impraticável, custando valores exorbitantes, até para a computação de grande porte da época. Mesmo sendo um computador planejado para um contexto corporativo, tratava-se de um dispositivo que nunca foi adotado de forma concreta no setor. Em 1981 a própria Xerox passou a produzir uma versão um pouco mais barata do sistema, que completo custaria cerca de US$100.000,00: o <a href="javascript:openMeta('star');">Xerox 8010 Star Information System</a>. Esta versão já funcionava com o um sistema operacional real, contando com uma interface gráfica, a View Point, já nos moldes do modelo <i>WIMP</i> (<i>Windows</i>, <i>Icons</i>, <i>Menus</i> & <i>Pointers</i>). Seu sistema foi o primeiro a funcionar em um contexto gráfico, mas sua penetração de mercado foi ínfima - menos de 25 mil unidades vendidas no mundo todo - tornando a inovação técnica pouco relevante e conhecida.</p>

            <p>Algo equivalente aconteceu também com o projeto <a href="javascript:openMeta('lisa');">Lisa</a> de Steve Jobs. A plataforma revolucionária criada pelo fundador da <a href="javascript:openMeta('apple');">Apple</a> também sofreu com um custo excessivo além de uma performance ruim. Apesar de custar um décimo do valor de um <a href="javascript:openMeta('star');">Xerox Star</a>, o <a href="javascript:openMeta('lisa');">Lisa</a> era um computador inacessível, principalmente nos termos da computação pessoal que eclodia no início da década de 1980. O <a href="javascript:openMeta('lisa');">Lisa</a> é geralmente considerado o marco real para o primeiro computador com uma interface gráfica com o usuário, mesmo que nunca tenha chegado efetivamente ao mercado consumidor; ele foi a base para o <a href="javascript:openMeta('macintosh');">Apple Macintosh</a>, esse sim, um sucesso comercial e um dispositivo de adoção massiva. O <a href="javascript:openMeta('lisa');">Lisa</a> pode ser considerado então como o protótipo para o Mac, e seu sistema operacional, o Lisa Office System 1.0, a base para o <a href="javascript:openMeta('macos');">MacOS System 1</a>.</p>

            <p>Foi, de fato, o <a href="javascript:openMeta('macos');">MacOS</a> quem trouxe em 1984 a interface gráfica com o usuário para as massas. Até essa data, o conceito de uma interface gráfica e do uso de um <i>mouse</i> para interagir com o dispositivo computacional eram práticas completamente desconhecidas pelo grande público. Esta é uma informação crítica para se compreender o quão revolucionário foi o VisiOn, já adotando uma interface gráfica própria um ano antes do conceito ser devidamente conhecido.</p>
