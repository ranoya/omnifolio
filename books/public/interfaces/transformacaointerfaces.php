            <p>Há, claramente, uma questão estética presente. A ferramenta buscava uma forma de expressão visual muito específica, que não era exatamente a de uma liberdade pictórica e imagética presente nas interfaces como a dos sistemas operacionais e <i>shells</i> gráficos, mas algo intermediário entre elas e as interfaces de texto. Duas versões subsequente do produto, e ele já operava em um contexto realmente gráfico, mas permanecia mimetizando o aspecto de um produto operando do <a href="./interfacetexto.php">ambiente de texto</a>, assim como diversos outros produtos também o fizeram, à exemplo do Norton Desktop (1991), o Microsoft Visual Basic (1992), o PC Tools 9.0 (1993), e o próprio MS-DOS 6.0 (1991).</p>



