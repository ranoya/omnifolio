            <p>Outra plataforma relevante no final da década de 1980 e início da década de 1990, que acabou esquecida, foi o GEM (1985), criada por <a target='_blank' href='https://www.youtube.com/embed/Tdj8gh9GPc4?si=75zh9H7W8xJdfsKF'>Gary Kildall</a>, fundador da Digital Research (DRI), após ver uma demonstração do Apple <a href="javascript:openMeta('lisa');">Lisa</a>. Ela teve mais exito como base para os sistemas operacionais dos computadores <a href="javascript:openMeta('amiga');">Amiga</a> e Atari ST, altamente competitivos na computação pessoal deste período, mas que foram perdendo espaço e mercado até o começo dos anos 2000. Uma versão do GEM também estava disponível para os IBM-PCs, mas a DRI foi processada pela <a href="javascript:openMeta('apple');">Apple</a> por copiar seus conceitos, e posteriormente impedida de implementás-los na versão para PC. A mesma coisa não aconteceu nas plataformas da <a href="javascript:openMeta('commodore');">Commodore</a> e da Atari, pois Steve Jobs não acreditava que estes equipamentos durariam muito tempo no mercado, ou lhe fariam qualquer concorrência (o que se provou verdadeiro apesar serem dispositivos que pareciam superiores aos PCs e Macs durante um bom tempo).</p>

