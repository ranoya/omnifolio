        <div class="corpus">
            <p>Obviamente que o Framework não teve o mesmo sucesso e importância do <a href="javascript:openMeta('dBASE');">dBASE</a>, mas pode-se dizer que foi uma ferramenta bem sucedida e devidamente conhecida. A Ashton-Tate acabou fragilizada na medida que clones do <a href="javascript:openMeta('dBASE');">dBASE III</a> se multiplicaram, e por conta de um desenvolvimento problemático da versão IV do mesmo software, muito mais pesada e complexa, abarrotada de <i>bugs</i> e erros. As dificuldades culminaram com a venda da empresa para a Borland, consagrada desenvolvedora de IDEs de programação, como o Turbo Pascal, o Turbo BASIC, e o Turbo C.</p>

            <p>Buscando se assemelhar ao VisiOn, a estrutura do Framework (mais especificamente o Framework II) se baseava em <i>Apps</i> independentes que poderiam ser construídos com uma linguagem de programação interna chamada de FRED. Cada ferramenta era considerada um <i>App</i>, integrados por um sistema de desktop que mimetizava, em um contexto de <a href="./interfacetexto.php">terminal de texto</a>, aquilo que se via no desktop de computadores como os <a href="javascript:openMeta('macintosh');">Macintoshs</a>.</p>

            <p>A inovação da interface ia além da inclusão de <a href='./dropdown.php'><i>menus drop down</i></a>, como um legado do próprio <a href="javascript:openMeta('dBASE');">dBASE III Plus</a>: o produto também implementava convenções como as de janelas que estavam presentes nas interfaces gráficas, no contexto de texto, permitindo que o espaço em tela de cada documento pudesse ser amplado, reduzido, deslocado, colocado no fundo ou à frente, exatamente como acontecia nos ambientes gráficos. A pilha de documentos abertos do VisiOn (que ficava no canto superior direito) foi copiada, mas alocada no canto inferior direito; mas seu funcionamento permanecia o mesmo: documentos minimizados que ficavam acessíveis para serem retomados em qualquer momento.</p>

