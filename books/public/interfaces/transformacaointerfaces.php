        <h2>Os herdeiros do VisiOn</h2>

        <div class="corpus">

            <p>Por anos fui fascinado pela interface do Framework II, suíte corporativa da Ashton-Tate lançada em 1984, mas que só vim a ter contato em meados de 1989. O desconhecimento e desinteresse das massas pelo VisiOn (ou pelo público corporativo, para o qual foi criado) é realmente um fato lamentável, mas ele foi bastante influente sobre os programadores e os profissionais de tecnologia da época, que o conheciam (e as suas inúmeras inovações) muito bem.</p>

            <p>A Ashton-Tate é mais um caso de uma empresa de software revolucionária que se perdeu com o tempo, como a Visicorp. Seu produto mais importante foi o <a href="javascript:openMeta('dBASE');">dBASE</a> (1980), o primeiro sistema de banco de dados relacional, e o pai da linguagem SQL utilizada até hoje para programar e fazer consultas nos bancos de dados. O <a href="javascript:openMeta('dBASE');">dBASE</a> foi, sem sombra de dúvidas, a segunda "killer application" da computação, depois do VisiCalc. Sua encarnação mais relevante foi o <a href="javascript:openMeta('dBASE');">dBASE III</a> de 1984, que estendia seus recursos à outras ferramentas, permitindo que softwares pudessem ser construídos utilizando seu banco de dados como estrutura principal. Pode-se afirmar que foi graças a ele que a gestão de pequenos negócios se informatizou, trazendo a computação utilizada pelas grandes corporações para o dia-a-dia dos comércios, do controle dos estoques, e de inúmeras atividades cotidianas que passaram a se beneficiar de registros facilmente recuperáveis com um controle bastante preciso.</p>

