        <div class="corpus">

            <p>Mas algumas descobertas realmente chamaram a atenção ao longo deste processo. A mais relevante delas diz respeito a um software praticamente desconhecido, cujas <a href="javascript:openMeta('razoesvision');">razões são incompreensíveis</a>: o Visicorp VisiOn. Trata-se de uma suíte integrada de ferramentas para escritório (processador de texto, planilha, banco de dados, etc.) com uma interface gráfica própria. O produto foi lançado um ano antes do primeiro concorrente, o Framework da Ashton-Tate, e três anos antes do principal concorrente, o Microsoft Works. Conta-se que Bill Gates, ao ver funcionamento do VisiOn na Comdex de 1982, teve certeza que o futuro da computação seria amparado no uso do <i>mouse</i> e de interfaces gráficas, acelerando o desenvolvimento do Microsoft Windows para que não perdesse a janela de oportunidade, e vislumbrando a necessidade imediata de um conjunto de ferramentas que viria a ser o Works, e futuramente o Microsoft Office.</p>

            <p>A Visicorp sempre foi uma empresa visionária e inovadora, mas com igual inabilidade para garantir a sustentabilidade de seus produtos. Foram os verdadeiros responsáveis pela revolução da computação pessoal com a invenção do VisiCalc, a primeira planilha eletrônica de cálculo. Steve Jobs afirmou que a <a href="javascript:openMeta('apple');">Apple</a> não seria "A Apple", se não houvesse o VisiCalc. A criação revolucionária de Dan Bricklin e Bob Frankston é a primeira "killer application" produzida, isto é, um software tão valioso e desejável que justificaria a aquisição de toda uma plataforma ou infra-estrutura específica de informática para que pudesse ser usado. Em 1979, o VisiCalc era este produto; em 1983 - mesmo ano que a VisiCorp lançou o VisiOn - o VisiCalc já havia ficado para trás e perdido espaço de mercado para o Lotus 1-2-3.</p>

            <p>Contudo, além da a inovação disruptiva com a criação de um novo segmento de mercado (as suítes corporativas), a Visicorp revolucionava as interfaces, com um produto real entregue um ano antes do <a href="javascript:openMeta('macos');">MacOS</a>, o a primeira interface gráfica com usuário em termos concretos.</p>

