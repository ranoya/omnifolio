        <h1 id="titulocapitulo">Mais considerações sobre as interfaces antigas</h1>

        <div class="corpus">

            <p>Desde 2021 tenho relizado um trabalho que chamo de <a target='_blank' href='https://omnidocs.vercel.app/materiais/arqueologiadesign'>arqueologia digital</a>, investigado artefatos antigos, alguns com mais de 40 anos de idade, no contexto computacional. Isso produziu um levantamento de softwares e ferramentas bastante desconhecidas para as gerações atuais, e, incrivelmente, a descoberta de alguns softwares e interfaces obscuras até para quem atuou profissionalmente no período.</p>

            <p>Com o levantamento foi possível compilar toda a transformação que as interfaces com usuários de dispositivos computacionais sofreram neste intervalo, e identificar elementos e expressões que se perderam ao longo de uma verdadeira guerra para a hegemonia de formatos e padrões para a computação. Esta compilação pode ser consultada abaixo.</p>

            <p>Me parece uma questão importante o conhecimento e a construção de um repertório contendo as interfaces do passado, pois projetar qualquer coisa, seja uma interface ou qualquer tipo de artefato, é um processo de consciência de escolhas feitas para a configuração deliberadamente atribuída. Projetar algo levado apenas por aquilo que se faz presente no momento não é uma forma consciente de design; é na verdade o oposto do design, com a manutenção de técnicas e processos dissociados dos motivos que levaram a eles em um primeiro momento.</p>

            <p>Se projetar algo é fazer escolhas livres, parece necessário conhecer a história das diversas configurações existentes para os artefatos e elementos projetados, tanto para que a escolha seja consciente e ciente das opções existentes, como para que não se reinventemos o passado acreditando estar propondo uma novidade.</p>

