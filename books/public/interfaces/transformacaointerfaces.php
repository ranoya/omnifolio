            <p>É fácil compreender que o VisiOn era um projeto muito à frente de seu tempo. Ele foi vítima de sua própria condição visionária, e de um mercado ainda não preparado para o futuro que chegaria em breve.</p>

            <p>Outro aspecto interessante de sua interface é a opção pelo uso de <i>menus</i> na parte inferior das janelas. Esta é uma escolha que nos parece estranha hoje, resultado da forma como os <a href='./dropdown.php'><i>menus drop down</i></a> prevaleceram enquanto convenção nas interfaces computacionais nos anos seguintes. Nos habituamos com esta convenção, o que torna a escolha da Visicorp um <i>pattern</i> diferente. Mas no início da década de 1980, estas convenções ainda não estavam formadas. Os softwares da Microsoft, como o Microsoft Word, o Microsoft Chart, o Multiplan, utilizavam uma estrutura de <i>menus</i> na parte inferior da tela, acessíveis através da tecla <i>ESC</i> (considere-se que os computadores não usavam <i>mouse</i>), alternados através da barra de espaço no teclado, e acionados através da tecla <i>Enter</i>. A condição de um <i>menu</i> pelo qual circulávamos passando de opção em opção até chegarmos na instrução desejada (sem a existência de um segundo ou terceiro nível hierárquico de opções) era um padrão bastante popular para os softwares daquele momento.</p>

            <p>O VisiOn inovou este modelo com um sistema de <i>menus</i> que podia ser deslocado horizontalmente dentro da janela, permitindo que todas as opções ficassem acessíveis, mesmo quando o espaço de tela disponível não o comportava. Esta foi uma solução bastante sofisticada para um problema de navegação imediato, antes do aparecimento da proposta mais simples de um <i>menu</i> fixo no topo da tela, alternado por cada software em foco dentro do sistema (solução que o Microsoft Windows, por exemplo, nunca adotou).</p>

            <p>Por fim, evidenciando inovação atrás de inovação, as ferramentas da suite (processador de texto, planilha, banco de dados, e outros) eram softwares independentes, instados separadamente do <i>File Manager</i>, mas que possuiam a capacidade de intercâmbio integrado de suas informações e dados, assim como feito pelo Microsoft Windows dois anos depois.</p>

