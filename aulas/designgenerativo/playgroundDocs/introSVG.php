<h1>Guia de programação em <strong>SVG</strong></h1>

  <p><span class="marca">SVG (Scalable Vector Graphics)</span> é uma especificação controlada pelo <span class="marca">W3C (World Wide Web Consortium)</span> para registro de imagens vetoriais de forma com que sejam universalmente compatíveis com quaisquer softwares ou plataformas. O formato <span class="marca">SVG</span> é, portanto, um grande avanço para tornar gráficos vetoriais mais acessíveis, que tornou possível, inclusive, a apresentação de imagens vetoriais pelos diversos navegadores, nativamente.</p>

  <p>Uma das grandes inovações estabelecidas pela especificação é o uso de uma estrutura/sintaxe <span class="marca">XML (Extensible Markup Language)</span>, tecnologia igualmente adotada para compatiblização de diversos sistemas e plataformas computacionais, com o intuito de tornar toda a troca de dados mais fácil e transparente entre eles. A estrutura <span class="marca">XML</span> dos arquivos <span class="marca">SVG</span> os tornam muito flexíveis: além de tornar possível que qualquer desenvolvedor de software incorpore o <span class="marca">SVG</span> em seus produtos (pois a especificação é pública, e não é proprietária de uma única empresa), também permite que as imagens criadas com SVG possam ser maniupadas através de programação, já que os códigos que as estruturam podem ser lidos e facilmente interpretados.</p>

  <p>Em decorrência destas características, o SVG aproxima a ferramenta de programação de outras ferramentas utilizadas por designers e artistas: é possível criar algo em uma ferramenta como <span class="marca">Adobe Illustrator</span>, abrir o arquivo por programação e efetuar mudanças na imagem resultante ou no próprio cógio, e voltar a trabalhar com ele em outro software como o <span class="marca">Sketch</span>, assim como é possível se criar algo inteiramente via programação e abrir seu resultado final em um software como <span class="marca">Adobe Illustrator</span> ou <span class="marca">Sketch</span> para um retoque final.</p>

  <p>O <span class="marca">SVG</span> está, portanto, no coração da transformação nas técnicas e ferramentas das tecnologias criativas.</p>
