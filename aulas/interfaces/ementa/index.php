    <h2><span class="indexnumber">3</span> Resumo<a name="resumo" id="ancresumo">&nbsp;</a></h2>
    <p>
      Esta disciplina trabalha com a criação e desenvolvimento de interfaces (geralmente gráficas) para artefatos
      digitais. Ela vai do processo de levantamento de necessidades dos usuários envolvidos até a prototipação da
      solução, podendo chegar, eventualmente, à fase de execução do projeto, dependendo de como a disciplina é
      articulada com outras atividades paralelas. O intuito deste grupo de estudo é aprofundar os princípios e
      processos utilizados no design de interfaces, e desenvolver as competências e habilidades necessárias para seu
      desenvolvimento.
    </p>


