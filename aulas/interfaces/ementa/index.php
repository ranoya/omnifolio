    <h2><span class="indexnumber">9</span> Ementa<a name="ementa" id="ancementa">&nbsp;</a>

    </h2>

    <p style="text-align: right">
      Eixo de tecnologia / produção<br>nível intermediário
    </p>
    <br>

    <p>
      Todo serviço ou produto digital é mediatizado através de um artefato cuja ênfase é servir de interface entre o
      serviço/produto e seu usuário. Sua construção pode se materializar de inúmeras formas, de um aplicativo mobile à
      um artefato de computação física, mas vasta gama de possibilidades materiais para sua configuração é estruturada
      por princípios comuns à mídia computacional, que respondem à seus aspectos particulares e especificidades. A
      configuração de uma interface com o usuário não pode ser tratada apenas como uma fase de projeto, ou como um
      projeto em separado do próprio serviço ou produto (como uma embalagem independente do próprio objeto que embala);
      sua configuração é parte da solução do próprio produto/serviço, e precisa ser construída junto com ele.
    </p>

    <h2><span class="indexnumber">10</span>  Objetivos<a name="objetivo" id="ancobjetivo">&nbsp;</a></h2>

    <p>Este grupo de estudos tem como objetivo preparar o estudante de design para trabalhar na intrincada solução das
      interfaces, que se inserem entre as necessidades de um usuário e o funcionamento de um serviço/produto, na
      tentativa de torná-los mais simples, compreensíveis e convenientes, ao mesmo tempo que os tornam mais funcionais
      e fluídos.</p>


