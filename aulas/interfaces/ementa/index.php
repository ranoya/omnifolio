    <h2 style="clear:both"><span class="indexnumber">13</span> Sistema de avaliação e critérios<a name="avaliacao" id="ancavaliacao">&nbsp;</a>

    </h2>

    <p>
      A avaliação do aprendizado é realizada utilizando os princípios definidos pela própria <a
        href="https://www.projetao.com.br/?g=Interfaces" class="links">metodologia Projetão</a>, feita continuamente
      sobre o desenvolvimento semanal do projeto (apresentações e pitchs). Estas apresentações parciais (ou a apresentação final dos resultados) podem ocorrer, inclusive, em outras disciplinas ou eventos, tais como no <a
        href="https://www.projetao.com.br/?g=Interfaces&dun=DemodayCIn20191&embed=plain#titlestart"
        class="links">Demoday de Projetão</a> ou
      em seus <a href="https://www.youtube.com/watch?list=PLQPGrXJqCAskipH8tq5SbTupbtJ_Y0DMf&v=FTa3PjkJ_Og"
        class="links">Pitchs</a>, no Seminário de PC1 ou apresentação de TCC, ou na
      disciplina de <a
        href="https://www.ranoya.com/public/aulas?theme=paleblue&iconhighlight=42148e&bgcolor=544DDF&forecolor=5048DA&#designdigital"
        class="links">Design de Serviços</a>.
      <br><br>
      O processo de avaliação é realizado em múltiplas frentes:<br><br>

      <span class="mininumber">1</span> Banca de Projeto: é a avaliação realizada por especialistas e profissionais de mercado durante o evento de <a href="https://www.projetao.com.br/dd/" target="_blank" class="links">Demoday</a>;
      <hr style="border: 0; width: 100%; clear: both; height: 15px;"></hr>

      <span class="mininumber">2</span> Avaliação de Maturidade de Projeto: é a avaliação feita à partir do acompanhamento semanal do projeto, disponível à todos os alunos na página do <a href="https://www.projetao.com.br/programa/?g=Interfaces"  target="_blank" class="links">programa de Projetão</a>;
      <hr style="border: 0; width: 100%; clear: both; height: 15px;"></hr>

      <span class="mininumber">3</span> Avaliação dos Mentores: é a percepção e o relato de peculiaridades enfrentadas pela equipe feito pelo mentor que acompanhou o projeto com mais proximidade;
      <hr style="border: 0; width: 100%; clear: both; height: 15px;"></hr>

      <span class="mininumber">4</span> Avaliação Geral do Projeto: é a avaliação geral realizada pelos professores das disciplinas, que leva em conta a completude do projeto desenvolvido, a presença de inovação, sua qualidade, complexidade, o processo pelo qual ele foi desenvolvido (Maturidade), a perspectiva dos Mentores, e as considerações da Banca de Projeto. Esta avaliação atribui uma nota de <b>0 à 10</b> para cada projeto apresentado com base nestes fatores.;
      <hr style="border: 0; width: 100%; clear: both; height: 15px;"></hr>

      <span class="mininumber">5</span> <a href="https://www.projetao.com.br/av360/?g=Interfaces" target="_blank" class="links">Avaliação 360 graus</a>: É a avaliação realizada sobre a participação de cada aluno por seus colegas de equipe. Esta avaliação modifica individualmente a nota geral de projeto em <b>2 pontos (para mais, ou para menos)</b>, resultando em uma avaliação particular para cada aluno;
      <hr style="border: 0; width: 100%; clear: both; height: 15px;"></hr>

      <br>
      Para um maior detalhamento sobre cada aspecto da avaliação, consulte o <a href="https://www.projetao.com.br/generator/?dun=avaliacoes&g=Interfaces" target="_blank" class="links">documento sobre as avaliações</a> de Projetão para uma explicação ampla de cada modalidade, e o documento do <a href="https://www.projetao.com.br/generator/?dun=maturidademilestones&g=Interfaces" target="_blank" class="links">Modelo de Maturidade</a> para o detalhamento dos critérios utilizados nesta forma específica de avaliação.<br><Br>
      

