<h1>Organização do código HTML</h1>

  <p>Por convenção, há uma estrutura básica que deveria ser obedecida em todo código HTML. Esta estrutura divide o código entre uma parte onde são feitas definições e registradas meta-informações sobre o documento chamada <code>&lt;head&gt;</code>, e uma parte onde se localiza aquilo que efetivamente será visto no código chamada <code>&lt;body&gt;</code>. A estrutura básica de um código HTML deve ser a seguinte:</p>

  <pre class='editor' data-name='editor_1' id='editor_1'>
  <!DOCTYPE html>
  &lt;html&gt;
    &lt;head&gt;
        defini&ccedil;&otilde;es
    &lt;/head&gt;

    &lt;body&gt;
        conte&uacute;dos
    &lt;/body&gt;
  &lt;/html&gt;
  </pre>

